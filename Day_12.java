
public class Day_12 {
    public static void main(String[]args) {
        // Operator Penugasan
        int a;
        int b;
        
        // pengisian nilai
        a = 20;
        b = 18;
        // penambahan
        b += a;
        // sekarang b = 18
        System.out.println("Penambahan : " + b);
        // pengurangan
        b -= a;
        // b = 18
        System.out.println("Pengurangan : " + b);
        
                
        
    }
    }