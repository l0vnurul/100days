public class Day50 {
    public static void main(String[] args) {
        // prulanganForEach
        
        // membuat array
        int angka[] = {4,2,6,8};
        // menggunakan perulangan for each untu menampilkan angka
        for( int x : angka ){
            System.out.print(x + " ");
        }
    }
    
}
