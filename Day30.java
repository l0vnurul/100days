public class Day30 {
    public static void main(String[] args) {
        int angka1 = 5;
        int angka2 = 20;
        
        // Operator perbandingan '==' untuk memeriksa kesamaan
        boolean sama = (angka1 == angka2);
        System.out.println("Apakah angka1 sama dengan angka2? " + sama);
        
        // Operator perbandingan '!=' untuk memeriksa ketidaksamaan
        boolean tidakSama = (angka1 != angka2);
        System.out.println("Apakah angka1 tidak sama dengan angka2? " + tidakSama);
        
        // Operator perbandingan '>=' untuk memeriksa lebih besar atau sama dengan
        var lebihBesarSamaDengan = (angka1 >= angka2);
        System.out.println("Apakah angka1 lebih besar atau sama dengan angka2? " + lebihBesarSamaDengan);
         // Operator perbandingan '<=' untuk memeriksa lebih lecil atau sama dengan
        var lebihKecilSamaDengan = (angka1 <= angka2);
        System.out.println("Apakah angka1 lebih kecil atau sama dengan angka? " + lebihKecilSamaDengan);
    }
}
